    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.getSelectedRow();
        namat.setText(jTable1.getModel().getValueAt(baris, 0).toString());
        namat1.setText(jTable1.getModel().getValueAt(baris, 1).toString());
        id = Integer.parseInt(jTable1.getModel().getValueAt(baris, 1).toString());

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (namat.getText().isEmpty() || namat1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Klik Tabel Bial ingin update");
        } else {
            try {
                dp.setbrand(jComboBox1.getSelectedItem().toString());
                dp.setkerusakan(jComboBox3.getSelectedItem().toString());
                dp.setno_resi(id);
                if (cont.upd_item(dp) == 1){
                    JOptionPane.showMessageDialog(null, "Data Laptop Ini Berhasil DiUpdate");   
                } else {
                    JOptionPane.showMessageDialog(null, "Data Laptop ini Tidak berhasil Diupdate");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        buattable();
        showtbl();
    }//GEN-LAST:event_jButton1ActionPerformed

