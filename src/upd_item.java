    private void combo_dmg() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/servis", "root", "");
            sql = "select * from item_dmg;";
            st = conn.prepareStatement(sql);
            rs = st.executeQuery();
            while (rs.next()) {
                String dmg = rs.getString("kerusakan");
                jComboBox3.addItem(dmg);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void combo_brnd() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/servis", "root", "");
            sql = "select * from tbl_konten;";
            st = conn.prepareStatement(sql);
            rs = st.executeQuery();
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("brand"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void buattable() {
        tbl = new DefaultTableModel();
        tbl.addColumn("Nama Clien");
        tbl.addColumn("Nomor Resi");
        tbl.addColumn("Brand");
        tbl.addColumn("Kerusakan");
        jTable1.setModel(tbl);
    }

    private void showtbl() {
        tbl.getDataVector().removeAllElements();
        tbl.fireTableDataChanged();
        listItem.clear();
        listItem = cont.tampil_item();

        for (i = 0; i < listItem.size(); i++) {
            Object[] data = new Object[4];
            data[0] = listItem.get(i).getnama_client();
            data[1] = listItem.get(i).getno_resi();
            data[2] = listItem.get(i).getbrand();
            data[3] = listItem.get(i).getkerusakan();
            tbl.addRow(data);
        }
    }

