    private void buattabel() {
        model = new DefaultTableModel();
        model.addColumn("no_id");
        model.addColumn("username");
        model.addColumn("password");
        model.addColumn("staff_stts");
        jTable1.setModel(model);
    }

    private void showtable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        listPengguna.clear();
        listPengguna = cont.tampil_admin();

        for (x = 0; x < listPengguna.size(); x++) {
            Object[] data = new Object[4];
            data[0] = listPengguna.get(x).getno_id();
            data[1] = listPengguna.get(x).getusername();
            data[2] = listPengguna.get(x).getpassword();
            data[3] = listPengguna.get(x).getstaff_stts();
            model.addRow(data);
        }
    }

