    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (laptop_brand.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Update Bisa Dilakukan kalau Field-nya Terisi", "Pesan", JOptionPane.WARNING_MESSAGE);
        } else {
            listLaptop = cont.cariLaptop(laptop_brand.getText());
            if (listLaptop.size() == 1) {
                JOptionPane.showMessageDialog(null, "Brand laptop ini telah di daftarkan sebelumnya", "Pesan", JOptionPane.WARNING_MESSAGE); 
                laptop_brand.requestFocus();       
            } else {
                try {
                    dp.setbrand(laptop_brand.getText());
                    if (cont.tambah_laptop(dp) == 1) {
                        JOptionPane.showMessageDialog(null, "Data Berhasil di tambahkan");
                    } else {
                        JOptionPane.showMessageDialog(null, "Data gagal Disimpan");
                    }
                } catch (Exception e) {
                }
            }
        }
        laptop_brand.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

